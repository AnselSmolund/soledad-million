export function calculateRankings(activities) {
  console.log("cl ");
  const assignRank = (sortedArr, key) => {
    return sortedArr.map((item, index) => ({
      ...item,
      [`${key}Rank`]: index + 1,
    }));
  };

  const distanceRanked = assignRank(
    [...activities].sort((a, b) => b.distance - a.distance),
    "distance"
  );

  console.log(distanceRanked);

  const timeRanked = assignRank(
    [...distanceRanked].sort((a, b) => b.elapsedTime - a.elapsedTime),
    "elapsedTime"
  );

  const elevationRanked = assignRank(
    [...timeRanked].sort((a, b) => b.elevationGain - a.elevationGain),
    "elevationGain"
  );

  return elevationRanked;
}

